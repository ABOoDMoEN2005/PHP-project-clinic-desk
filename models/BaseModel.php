// =============================================================
// ملف BaseModel.php - الكلاس الأساسي لكل الـ Models
//
// شو المفروض تعمله الـ Model؟
// كل تعامل مع قاعدة البيانات يصير هون بالـ Model
// الـ Controller ما يكتب SQL مباشرة
// الـ View ما تتصل بقاعدة البيانات أبداً
//
// BaseModel بيحتوي على الـ $db (الاتصال) وطريقة execute()
// المشتركة بين كل الـ models
// =============================================================

// نجيب الـ Database class
require_once __DIR__ . '/../core/Database.php';

// abstract يعني ما نقدر نعمل new BaseModel() مباشرة
// لازم نوجده من كلاس تاني بيرثه
abstract class BaseModel
{
    // الاتصال بقاعدة البيانات - protected يعني الكلاسات الوارثة بتوصله
    protected Database $db;

    // =============================================================
    // Constructor - بنجيب instance الـ database مرة واحدة
    // =============================================================
    public function __construct()
    {
        // getInstance() بترجع نفس الاتصال الموجود مش تعمل جديد
        // هاد هو فائدة الـ Singleton Pattern
        $this->db = Database::getInstance();
    }

    // =============================================================
    // execute() - طريقة مشتركة لتنفيذ الاستعلامات
    //
    // ليش عندنا execute() هون بدل نستدعي $this->db->query() مباشرة؟
    // عشان لو يوم من الأيام حبينا نغير طريقة التعامل مع الأخطاء
    // أو نضيف logging، نغيرها هون مكان واحد بس وكل الـ models بستفيدوا
    // =============================================================
    protected function execute(string $sql, string $types = '', array $params = [])
    {
        // بنحول الاستعلام لـ $this->db
        return $this->db->query($sql, $types, $params);
    }
}
