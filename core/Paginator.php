// =============================================================
// ملف Paginator.php - إدارة التصفح بين الصفحات (Pagination)
//
// بدلاً من نجيب كل السجلات من قاعدة البيانات دفعة واحدة
// وهاد ممكن يكون بطيء جداً لو في ألاف السجلات،
// بنجيب بس قدر معين كل مرة (مثلاً 10 سجلات)
//
// بنستخدم LIMIT و OFFSET بالـ SQL:
// - LIMIT = كم سجل نجيب
// - OFFSET = من أين نبدأ
// =============================================================

class Paginator
{
    // العدد الكلي للسجلات في قاعدة البيانات
    private int $totalItems;

    // كم سجل نعرض بكل صفحة
    private int $perPage;

    // رقم الصفحة الحالية
    private int $currentPage;

    // =============================================================
    // Constructor - نجهز الـ Paginator بالأرقام المطلوبة
    // =============================================================
    public function __construct(int $totalItems, int $perPage, int $currentPage)
    {
        $this->totalItems  = $totalItems;
        $this->perPage     = $perPage;

        // نتأكد إن رقم الصفحة صح - مش أقل من 1
        $this->currentPage = max(1, $currentPage);
    }

    // =============================================================
    // offset() - بيحسب من أين نبدأ بقاعدة البيانات
    //
    // مثلاً: الصفحة 3، 10 عناصر بكل صفحة
    // offset = (3-1) * 10 = 20
    // يعني نتخطى أول 20 سجل ونجيب من الـ 21
    // =============================================================
    public function offset(): int
    {
        return ($this->currentPage - 1) * $this->perPage;
    }

    // =============================================================
    // totalPages() - العدد الكلي للصفحات
    //
    // مثلاً: 25 سجل، 10 بكل صفحة
    // ceil(25/10) = ceil(2.5) = 3 صفحات
    // =============================================================
    public function totalPages(): int
    {
        // ceil بترجع عدد صحيح للأعلى
        // نستخدمها لأن 25/10 = 2.5 ومحتاج 3 صفحات فعلياً
        return (int) ceil($this->totalItems / $this->perPage);
    }

    // =============================================================
    // hasPrev() - هل في صفحة سابقة؟
    // =============================================================
    public function hasPrev(): bool
    {
        return $this->currentPage > 1;
    }

    // =============================================================
    // hasNext() - هل في صفحة تالية؟
    // =============================================================
    public function hasNext(): bool
    {
        return $this->currentPage < $this->totalPages();
    }

    // =============================================================
    // هاي Getters بسيطة عشان نوصل للأرقام من الـ view
    // =============================================================
    public function getCurrentPage(): int
    {
        return $this->currentPage;
    }

    public function getPerPage(): int
    {
        return $this->perPage;
    }

    public function getTotalItems(): int
    {
        return $this->totalItems;
    }
}
